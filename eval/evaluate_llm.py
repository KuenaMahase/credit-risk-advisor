"""
LLM answer evaluation: compare prompt variants with an LLM-as-a-judge.

Follows the course's A->Q->A' setup, adapted to a chunked corpus:
  A  = the source chunk a ground-truth question was generated from
  Q  = the ground-truth question (eval/ground_truth.csv)
  A' = the answer produced by the full RAG flow (rerank retrieval + LLM)

For each prompt variant, the RAG flow answers a seeded sample of ground-truth
questions; a judge model then marks each answer good/bad against the source
chunk. Refusals count as bad when the source chunk does answer the question —
so retrieval misses are folded into end-to-end answer quality, which is what
this evaluation measures.

Caveat: the judge is the same model family as the generator (gpt-4o-mini
judging gpt-4o-mini), a standard course-project setup but a known bias.

Run:
    python -m eval.evaluate_llm

Output:
    eval/llm_answers.csv   (per-question answers + verdicts, all variants)
    eval/llm_results.csv   (variant, good_rate, n)
"""

from __future__ import annotations

import csv
import json
import random
import sys
import time
from concurrent.futures import ThreadPoolExecutor
from pathlib import Path
from typing import Literal

from pydantic import BaseModel
from tqdm import tqdm

from rag.llm import (
    INSTRUCTIONS_CITED,
    INSTRUCTIONS_QUOTE_FIRST,
    build_prompt,
    get_client,
    llm,
)
from rag.search import SEARCH_MODES

ROOT = Path(__file__).resolve().parent.parent
GROUND_TRUTH_FILE = ROOT / "eval" / "ground_truth.csv"
CHUNKS_FILE = ROOT / "data" / "processed" / "chunks.jsonl"
ANSWERS_FILE = ROOT / "eval" / "llm_answers.csv"
RESULTS_FILE = ROOT / "eval" / "llm_results.csv"

JUDGE_MODEL = "gpt-4o-mini"
SEARCH_MODE = "rerank"  # the retrieval-eval winner; keep both variants on it
SAMPLE_SIZE = 200
SEED = 7
MAX_WORKERS = 6

# Both prompts are defined in rag/llm.py; the eval decides which one is
# production (INSTRUCTIONS points at the winner there).
PROMPT_VARIANTS = {
    "v1_cited": INSTRUCTIONS_CITED,
    "v2_quote_first": INSTRUCTIONS_QUOTE_FIRST,
}


class AnswerEvaluation(BaseModel):
    reasoning: str
    verdict: Literal["good", "bad"]


JUDGE_INSTRUCTIONS = """You are an expert evaluator for a RAG system over the Basel III
credit-risk framework. You will be given:
1. A question from an analyst
2. The source excerpt the question was generated from (ground truth)
3. An answer generated by an AI assistant

Decide if the AI answer correctly answers the question, consistent with the
source excerpt.

Rules:
- The answer does not need to quote the excerpt; extra detail, citations, and
  caveats are fine as long as the core answer is correct.
- Mark 'bad' if the answer is wrong, contradicts the excerpt, or misses the
  key point.
- If the AI declined to answer but the excerpt does answer the question,
  mark 'bad'.
Be fair and focus on correctness, not style.""".strip()

JUDGE_PROMPT = """Question:
{question}

Source excerpt (ground truth):
{source_text}

AI answer:
{answer}""".strip()


def load_chunk_index() -> dict[str, dict]:
    with open(CHUNKS_FILE) as f:
        chunks = [json.loads(line) for line in f if line.strip()]
    return {c["chunk_id"]: c for c in chunks}


def generate_answer(instructions: str, question: str) -> str:
    chunks = SEARCH_MODES[SEARCH_MODE](question)
    prompt = build_prompt(question, chunks)
    return llm(prompt, instructions=instructions)


def judge_answer(question: str, source_text: str, answer: str,
                 max_retries: int = 3) -> AnswerEvaluation:
    prompt = JUDGE_PROMPT.format(
        question=question, source_text=source_text, answer=answer
    )
    for attempt in range(max_retries):
        try:
            response = get_client().responses.parse(
                model=JUDGE_MODEL,
                input=[
                    {"role": "developer", "content": JUDGE_INSTRUCTIONS},
                    {"role": "user", "content": prompt},
                ],
                text_format=AnswerEvaluation,
            )
            return response.output_parsed
        except Exception:  # noqa: BLE001
            if attempt == max_retries - 1:
                raise
            time.sleep(2**attempt)
    raise RuntimeError("unreachable")


def run_variant(variant: str, instructions: str, sample: list[dict],
                chunk_index: dict[str, dict]) -> list[dict]:
    def process(q: dict) -> dict:
        source_text = chunk_index[q["chunk_id"]]["text"]
        answer = generate_answer(instructions, q["question"])
        evaluation = judge_answer(q["question"], source_text, answer)
        return {
            "variant": variant,
            "question": q["question"],
            "chunk_id": q["chunk_id"],
            "answer": answer,
            "verdict": evaluation.verdict,
            "reasoning": evaluation.reasoning,
        }

    with ThreadPoolExecutor(max_workers=MAX_WORKERS) as pool:
        return list(tqdm(pool.map(process, sample), total=len(sample), desc=variant))


def main() -> int:
    with open(GROUND_TRUTH_FILE) as f:
        ground_truth = list(csv.DictReader(f))
    random.seed(SEED)
    sample = random.sample(ground_truth, min(SAMPLE_SIZE, len(ground_truth)))
    chunk_index = load_chunk_index()

    print(f"Evaluating {len(PROMPT_VARIANTS)} prompt variants on "
          f"{len(sample)} questions (search mode: {SEARCH_MODE}, judge: {JUDGE_MODEL})\n")

    # Warm the lazy index/model singletons serially before the thread pool:
    # concurrent first calls would each build their own copy (lru_cache is not
    # locked) and race on the HuggingFace model cache.
    SEARCH_MODES[SEARCH_MODE]("warm-up query")

    all_rows: list[dict] = []
    summary: list[dict] = []
    for variant, instructions in PROMPT_VARIANTS.items():
        rows = run_variant(variant, instructions, sample, chunk_index)
        all_rows.extend(rows)
        good = sum(1 for r in rows if r["verdict"] == "good")
        good_rate = good / len(rows)
        summary.append({"variant": variant, "good_rate": good_rate, "n": len(rows)})
        print(f"{variant}: good_rate={good_rate:.3f} ({good}/{len(rows)})")

    with open(ANSWERS_FILE, "w", newline="") as f:
        writer = csv.DictWriter(
            f, fieldnames=["variant", "question", "chunk_id", "answer", "verdict", "reasoning"]
        )
        writer.writeheader()
        writer.writerows(all_rows)

    with open(RESULTS_FILE, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=["variant", "good_rate", "n"])
        writer.writeheader()
        writer.writerows(summary)

    print(f"\nWrote {len(all_rows)} judged answers -> {ANSWERS_FILE}")
    print(f"Wrote summary -> {RESULTS_FILE}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
